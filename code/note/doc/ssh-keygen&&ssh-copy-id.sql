你要在 192.168.10.101 与 192.168.10.143 两台服务器之间互相免密登录（即 SSH key 认证），可以按下面步骤操作：

1. 在两台机器上生成 SSH key

（假设你使用的是 root 或某个普通用户，保持一致即可）

在 101 和 143 上都执行：

ssh-keygen -t rsa -b 4096 -C "免密登录"
                                     -t rsa
                                     
                                     指定密钥算法类型为 RSA。
                                     
                                     常见类型还有：
                                     
                                     dsa（已过时，不推荐）
                                     
                                     ecdsa
                                     
                                     ed25519（更安全更快，推荐新项目使用）
                                     
                                     在这里用 rsa，是兼容性最强的选择。
                                     
                                     -b 4096
                                     
                                     指定密钥长度（bit 数）。
                                     
                                     默认是 2048 位，这里设为 4096 位，更安全，但生成和验证会稍慢。
                                     
                                     -C "免密登录"
                                     
                                     为密钥添加 注释（comment），一般用于标识这把密钥。
                                     
                                     例如可以写邮箱、用户名或用途。
                                     
                                     执行后生成的公钥文件（id_rsa.pub）最后一段就是这个注释：

一路回车，不设置密码。
生成的公钥在 ~/.ssh/id_rsa.pub，私钥在 ~/.ssh/id_rsa。


2. 互相分发公钥
在 101 上，把公钥传到 143：
ssh-copy-id -i ~/.ssh/id_rsa.pub licz.1@192.168.10.143

在 143 上，把公钥传到 101：
ssh-copy-id -i ~/.ssh/id_rsa.pub licz.1@192.168.10.101


⚠️ user 换成你实际登录的用户名（比如 root）。


                            远程保存路径 ❌ 不可直接自定义

                             ssh-copy-id 的设计是 始终把公钥追加到远程用户的：
                             
                             ~/.ssh/authorized_keys
                             
                             
                             如果是 user@192.168.10.143 → /home/user/.ssh/authorized_keys
                             
                             如果是 root@192.168.10.143 → /root/.ssh/authorized_keys
                             
                             这个路径是写死的，不能直接通过参数改。

3. 验证免密登录

在 101 上：

ssh user@192.168.10.143


应当直接进入，不再要求输入密码。

在 143 上：

ssh user@192.168.10.101


同样应当免密。


